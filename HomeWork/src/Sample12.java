public class Sample12 {
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 5;
        System.out.println("num1和num2的各種運算:");
        System.out.println("num1+num2等於"+(num1+num2));
        System.out.println("num1+num2等於"+(num1-num2));
        System.out.println("num1+num2等於"+(num1*num2));
        System.out.println("num1+num2等於"+(num1/num2));
        System.out.println("num1+num2等於"+(num1%num2));
    }
}
